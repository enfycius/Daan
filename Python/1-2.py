"""Life is too short, You need Python"""

# 작은따옴표를 3개를 연속으로 써서 양쪽 둘러싸도 문자열이에요.

'''Life is too short, You need Python'''

a = "Life is too short, You need Python"

print(a)

b = '"Python is very easy." he says.'

print(b)

c = "'Python is very easy.' he says."

print(c)

# 큰따옴표나 작은따옴표 앞에 백슬래시(\)를 작성하게 되면, 이것은 문자열의 시작과 끝을 나타내는 용도로 사용되는 것이
# 아니라 단순히 그 문자를 출력해주는 용도로 사용되는 거에요,,,

